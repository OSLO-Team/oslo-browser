export function applyLanguage(lang) {
  state.currentLang = lang || 'tr';

  const settingsLanguage = document.getElementById('settings-language');
  if (settingsLanguage) {
    settingsLanguage.value = state.currentLang;
  }

  // Update text content with [data-i18n]
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.dataset.i18n;
    if (translations[state.currentLang] && translations[state.currentLang][key]) {
      el.textContent = translations[state.currentLang][key];
    }
  });

  // Update placeholders with [data-i18n-placeholder]
  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (translations[state.currentLang] && translations[state.currentLang][key]) {
      el.placeholder = translations[state.currentLang][key];
    }
  });

  // Update addressInput placeholder
  const addressInput = document.getElementById('address-input');
  if (addressInput) {
    addressInput.placeholder = translations[state.currentLang]['address-placeholder'];
  }

  // Update sidebar tooltips/titles
  const newTabBtn = document.getElementById('new-tab-btn');
  if (newTabBtn) newTabBtn.title = translations[state.currentLang]['new-tab'];

  const incognitoBtn = document.getElementById('incognito-btn');
  if (incognitoBtn) incognitoBtn.title = translations[state.currentLang]['incognito-tab'];

  const bookmarksBtn = document.getElementById('bookmarks-btn');
  if (bookmarksBtn) bookmarksBtn.title = translations[state.currentLang]['bookmarks'];

  const historyBtn = document.getElementById('history-btn');
  if (historyBtn) historyBtn.title = translations[state.currentLang]['history'];

  const downloadsBtn = document.getElementById('downloads-btn');
  if (downloadsBtn) downloadsBtn.title = translations[state.currentLang]['downloads'];

  const settingsBtn = document.getElementById('settings-btn');
  if (settingsBtn) settingsBtn.title = translations[state.currentLang]['settings'];

  const navHome = document.getElementById('nav-home');
  const navBack = document.getElementById('nav-back');
  const navForward = document.getElementById('nav-forward');
  const navReload = document.getElementById('nav-reload');
  const sidebarToggle = document.getElementById('sidebar-toggle');

  if (navHome) navHome.title = translations[state.currentLang]['home-btn-title'] || 'Anasayfa';
  if (navBack) navBack.title = translations[state.currentLang]['nav-back-title'] || 'Geri';
  if (navForward) navForward.title = translations[state.currentLang]['nav-forward-title'] || 'İleri';
  if (navReload) navReload.title = translations[state.currentLang]['nav-reload-title'] || 'Yenile';
  if (sidebarToggle) sidebarToggle.title = translations[state.currentLang]['toggle-sidebar'] || 'Menüyü Daralt/Genişlet';

  // Trigger real-time renders for panels to refresh texts
  renderBookmarks();
  renderBookmarksBar();
  renderHistory();
  renderDownloads();
  updateBookmarkIcon();
  if (typeof window.renderSpaces === 'function') {
    window.renderSpaces();
  }

  // Dispatch storage event to notify other windows (e.g. New Tab)
  window.dispatchEvent(new Event('storage'));
  window.dispatchEvent(new CustomEvent('language-changed', { detail: lang }));
}
